    private void btnUpdateDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateDetailsActionPerformed
        // TODO add your handling code here:
        StudentInitialPage trail = new StudentInitialPage();
        String LoggedPerson = trail.getVariable();
        for(StudDetails StudentSearch:NewStudentArray){
            if(StudentSearch.getUsername().equals(LoggedPerson)){
                StudentSearch.setFirstName(txtFirstName.getText());
                StudentSearch.setLastName(txtLastName.getText());
                StudentSearch.setAge(Integer.parseInt(txtAge.getText()));
                StudentSearch.setGender(txtGender.getText());
                StudentSearch.setAddress(txtAddress.getText());
                StudentSearch.setPhoneNumber(Double.parseDouble(txtPhoneNumber.getText()));
                StudentSearch.setRoomNumber(txtRoomNumber.getText());
                StudentSearch.setEmailId(txtEmailID.getText());
                break;
                
            }
            
        }
        StudCreate.setArrayLst(NewStudentArray);
        JOptionPane.showMessageDialog(this, "Updated Successfully");

    }//GEN-LAST:event_btnUpdateDetailsActionPerformed

    private void txtFirstNameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFirstNameKeyPressed
        // TODO add your handling code here:
        char c = evt.getKeyChar();
	if(Character.isLetter(c)|| Character.isISOControl(c) || Character.isWhitespace(c) )
    {
        txtFirstName.setEditable(true);
    }
    else
    {
        txtFirstName.setEditable(false);
        JOptionPane.showMessageDialog(this, "Invalid Input \nenter valid firstname");
    }
    }//GEN-LAST:event_txtFirstNameKeyPressed

