    private void populateAppointmentTable() {
        DefaultTableModel model = (DefaultTableModel) tblAppointments.getModel();
        model.setRowCount(0);
        
        for(BookAppointment ba : ecoSystem.getBookAppointmentDirectory().getBookAppointmentDirectory()) {
            if(ba.getStudentID().equals(userLogged.getPersonId())){
                Object[] row = new Object[3];
                row[0] = ba;
                String teacherName = ecoSystem.getTeacherDirectory().getTeacherNameByID(ba.getTeacherID());
                row[1] = teacherName;
                row[2] = ba.getTopic();
                model.addRow(row);
            }
        }
    }

    private void populateMealTable() {
        DefaultTableModel model = (DefaultTableModel) tblMealPlan.getModel();
        model.setRowCount(0);
        for (MealPlan mp : ecoSystem.getMealPlanDirectoryRef().getMealPlanDirectory()) {
            Object[] rowData = new Object[4];
            rowData[0] = mp;
            rowData[1] = mp.getMealPlan();
            rowData[2] = mp.getMealType();
            rowData[3] = mp.getMealDescription();
            model.addRow(rowData);
        }
    }
    
