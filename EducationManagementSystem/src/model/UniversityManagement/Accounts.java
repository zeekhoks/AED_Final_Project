public class Accounts {
    
    private float salary;
    private float studentFees;
    private int creditHours;
    private float perCreditFees;

    public float getSalary() {
        return salary;
    }

    public void setSalary(float salary) {
        this.salary = salary;
    }

    public float getStudentFees() {
        return studentFees;
    }

    public void setStudentFees(float studentFees) {
        this.studentFees = studentFees;
    }

    public int getCreditHours() {
        return creditHours;
    }

    public void setCreditHours(int creditHours) {
        this.creditHours = creditHours;
    }

    public float getPerCreditFees() {
        return perCreditFees;
    }

    public void setPerCreditFees(float perCreditFees) {
        this.perCreditFees = perCreditFees;
    }
    
    
    
}
